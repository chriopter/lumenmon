#!/bin/bash
# Draw dashboard header

draw_header() {
    local width=${1:-80}

    echo -e "${CYAN}${BOLD}┌─ LUMENMON ─────────────────────────────────────────────────────────────────┐${NC}"
}

draw_section_header() {
    local title="$1"
    echo -e "${CYAN}├─ ${BOLD}${title}${NC}${CYAN} ────────────────────────────────────────────────────────────────────┤${NC}"
}

draw_divider() {
    echo -e "${CYAN}├────────────────────────────────────────────────────────────────────────────┤${NC}"
}

draw_bottom() {
    echo -e "${CYAN}└────────────────────────────────────────────────────────────────────────────┘${NC}"
}
